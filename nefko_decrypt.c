/* NEF white balance/other data encryption algorithm
 * 1. Grab the count of shutter depressions from MakerNote. Use this
 *    to find the initial vector used for de-obfuscating.
 *      uint8_t iv = nef_iv_table[BYTE0(ShutterCount) ^ 
                                  BYTE1(ShutterCount) ^
                                  BYTE3(ShutterCount) ^
                                  BYTE2(ShutterCount)];
 * 2. Grab the camera serial number from MakerNote. Convert to integer, and
 *    use the 8 LSBs of it to compute the key.
 *      uint8_t key = nef_key[Serial & 0xff];
 * 3. Read in the obfuscated section of the file into buffer buf[] of length
 *    buf_len;
 * 4. counter = iv;
 *    for i = 0 to buf_len:
 *          counter += key * (i + 0x60);
 *          buf[i] ^= counter;
 *    done
 * 5. Buf now contains the de-obfuscated data.
 */

NEF_STATUS nef_decrypt_buffer(nef_t *fp,
                              void *buffer, size_t bytes)
{
    size_t i = 0;
    uint8_t *buf = (uint8_t *)buffer;
    uint8_t cntr;

    NEF_CHECK_ARG(buffer);
    NEF_CHECK_ARG(fp);

    if (bytes == 0) return NEF_RANGE_ERROR;

    cntr = fp->iv;

    for (i = 0; i < bytes; i++) {
        cntr += fp->key * (i + 0x60);
        buf[i] ^= cntr;
    }

    return NEF_OK;
}

NEF_STATUS nef_get_obfuscation_params(nef_t *nef)
{
    unsigned shutter_dep = 0;
    char *serial = NULL;
    int ser_type, ser_count;
    unsigned serial_num = 0;

    unsigned iv_off;

    NEF_CHECK_ARG(nef);

    /* Grab the shutter count tag */
    NEFKO_CHECK(nef_get_tag_low(nef, nef->makernote, TIFF_TAG_MAKERNOTE_SHUTTER,
                                &shutter_dep), NEF_NOT_FOUND);

    NEF_TRACE("Shutter count: %08x (%u)\n", shutter_dep, shutter_dep);

    NEFKO_CHECK(nef_get_tag_alloc(nef, nef->makernote, TIFF_TAG_MAKERNOTE_SERIAL,
                                  (void *)&serial, &ser_type, &ser_count),
                                  NEF_NOT_FOUND);

    if (ser_type != TIFF_TYPE_ASCII || ser_count <= 0) {
        NEF_TRACE("Failed to get serial number.\n");
        free(serial);
        return NEF_NOT_FOUND;
    }

    serial_num = strtol(serial, NULL, 10);

    NEF_TRACE("Camera serial number: %u\n", serial_num);

    free(serial);

    iv_off = BYTE(shutter_dep, 0) ^
             BYTE(shutter_dep, 1) ^
             BYTE(shutter_dep, 3) ^
             BYTE(shutter_dep, 2);

    nef->iv = nef_iv_table[iv_off];
    nef->key = nef_key[serial_num & 0xff];

    NEF_TRACE("iv = %02x, key = %02x\n", (unsigned)nef->iv,
        (unsigned)nef->key);

    return NEF_OK;
}

